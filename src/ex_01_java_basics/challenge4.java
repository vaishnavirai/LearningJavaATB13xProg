package ex_01_java_basics;

public class challenge4 {
    public static void main(String[] args) {
        int i = 5;

        // Post-increment (i++)
        System.out.println("Value used (i++): " + (i++));
        System.out.println("Value after i++: " + i);

        // Pre-increment (++i)
        System.out.println("Value used (++i): " + (++i));
        System.out.println("Value after ++i: " + i);

        // Post-decrement (i--)

        System.out.println("Value used (i--): " + (i--));
        System.out.println("Value after i--: " + i);

        // Pre-decrement (--i)

        System.out.println("Value used (--i): " + (--i));
        System.out.println("Value after --i: " + i);
    }
}
